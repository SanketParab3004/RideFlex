class Car {
    constructor(
      id,
      name,
      model,
      address,
      price,
      description,
      specifications,
      images,
      seats,
      carType,
      transmission,
      deliveryType,
      startDate,
      endDate
    ) {
      this.id = id;
      this.name = name;
      this.model = model;
      this.address = address;
      this.price = price;
      this.description = description;
      this.specifications = specifications;
      this.images = images;
      this.seats = seats;
      this.carType = carType;
      this.transmission = transmission;
      this.deliveryType = deliveryType;
      this.startDate = startDate;
      this.endDate = endDate;
    }
  }


export default Car;